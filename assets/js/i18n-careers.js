// Function to apply translations based on the selected language
window.applyTranslations = function(lang) {
  console.log('Applying translations for language:', lang);
  
  // Apply translations to all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (window.translations[lang] && window.translations[lang][key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = window.translations[lang][key];
      } else {
        element.textContent = window.translations[lang][key];
      }
    }
  });
  
  // Set page title
  if (window.translations[lang]['careers.page.title']) {
    document.title = window.translations[lang]['careers.page.title'];
  }
};

// Apply translations to the page on load
document.addEventListener('DOMContentLoaded', function() {
  // Get the current language
  const currentLang = localStorage.getItem('bixingLanguage') || 'en';
  
  // Apply translations
  window.applyTranslations(currentLang);
  
  // Update language toggle button text
  const currentLanguageElement = document.getElementById('currentLanguage');
  if (currentLanguageElement) {
    currentLanguageElement.textContent = currentLang === 'en' ? 'EN' : '中文';
  }
});
